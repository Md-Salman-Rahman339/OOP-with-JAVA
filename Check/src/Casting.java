public class  Casting {
    public static void main(String [] args){
        //widening Casting
        int myInt=9;
        double myDouble=myInt;
        System.out.println(myInt);
        System.out.println(myDouble);

//        narrow casting

        double double2=34d;
        int n=(int) (double2);
        System.out.println(n);
        System.out.println(double2);

    }
}

